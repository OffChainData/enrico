	public function getMarchEquinox($year) {
		return $this->getEquinox($year, 3, EquinoxUtils::$marchEquinoxUTCDates);
	}
	
	public function getSeptemberEquinox($year) {
		return $this->getEquinox($year, 9, EquinoxUtils::$septemberEquinoxUTCDates);
	}
	
	private function getEquinox($year, $month, $ar) {
		$retVal = NULL;
		if(array_key_exists($year, $ar)) {
			$triple = $ar[$year];
			$retVal = new EnricoDate($triple[0], $month, $year);
			$retVal->hour = $triple[1];
			$retVal->minute = $triple[2];
		}
		return $retVal;
	}
